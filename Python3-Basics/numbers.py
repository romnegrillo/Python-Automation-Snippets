num1 = 2
num2 = 3

# Arithmetic operators.
print("Addition: {}".format(num1 + num2))
print("Subtraction: {}".format(num1 - num2))
print("Multiplication: {}".format(num1 * num2))
print("Division: {}".format(num1 / num2))
print("Float Division: {}".format(num1 // num2))
print("Integer Division: {}".format(num1 % num2))
print("Exponential: {}".format(num1**num2))   

# Use parenthesis to overide operator presedence.
print(4*2+1)
print(4*(2+1))

# Comparison Operators
print(num1==num2)
print(num1>num2)
print(num1<num2)
print(num1>=num2)
print(num1<=num2)
